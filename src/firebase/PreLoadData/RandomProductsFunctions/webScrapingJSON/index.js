const formatData = (data) => {
    const formattedData = [];
    let animalCategory = data.animalCategory ? data.animalCategory : '';
    let category = data.category ? data.category : '';
    let subCategory = data.subCategory ? data.subCategory : '';

    let comments = [];


    data.title.forEach(element => {
        const { brand, name, price, image } = element;
        formattedData.push({
            animalCategory,
            category,
            subCategory,
            brand,
            name,
            price,

            image,
            comments,

            image

        });
    });
    return formattedData;
}

// Combina un array de arrays en un solo array
const combineArrays = (arrayOfArrays) => {
    const combinedArray = [];
    arrayOfArrays.forEach(array => {
        combinedArray.push(...array);
    });
    return combinedArray;
}

